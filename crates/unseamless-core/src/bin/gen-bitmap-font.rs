//! Code generator for [`unseamless_core::bitmap_font`]'s static glyph tables.
//!
//! Parses the vendored Proggy BDF bitmaps (`assets/fonts/`), merges each glyph's lit pixels into the
//! fewest rectangles ([`merge`]), and rewrites `src/bitmap_font/generated.rs` with the result. The
//! output is committed; this only runs when regenerating (e.g. after changing the vendored fonts or
//! the covered charset).
//!
//! It is gated behind the `gen-bitmap-font` feature (so it isn't cross-compiled on every build), and
//! the workspace's default target is `x86_64-pc-windows-gnu`, so run it against the host triple:
//!
//! ```sh
//! cargo run -p unseamless-core --bin gen-bitmap-font \
//!   --features gen-bitmap-font --target x86_64-unknown-linux-gnu
//! ```
//!
//! It writes the file in place; re-run the core tests afterwards (`scripts/test-core.sh`) — the
//! `*_matches_*_bdf` tests assert the committed tables still match the BDF sources.

use std::fmt::Write as _;
use std::path::Path;

use unseamless_core::bitmap_font::bdf::{self, BdfFont};
use unseamless_core::bitmap_font::merge;

fn main() {
    // Resolve paths relative to this crate, so it runs from anywhere.
    let crate_dir = Path::new(env!("CARGO_MANIFEST_DIR"));
    let menu_bdf = crate_dir.join("assets/fonts/proggy-clean-ascii.bdf");
    let compact_bdf = crate_dir.join("assets/fonts/proggy-tiny-ascii.bdf");
    let out = crate_dir.join("src/bitmap_font/generated.rs");

    let menu = load(&menu_bdf);
    let compact = load(&compact_bdf);

    let mut src = String::new();
    writeln!(
        src,
        "//! GENERATED by `gen-bitmap-font` — DO NOT EDIT BY HAND.\n\
         //!\n\
         //! Regenerate with:\n\
         //!   cargo run -p unseamless-core --bin gen-bitmap-font \\\n\
         //!     --features gen-bitmap-font --target x86_64-unknown-linux-gnu\n\
         //!\n\
         //! Source: vendored Proggy BDF bitmaps under `assets/fonts/` (MIT, Tristan Grimmer).\n\
         //! Each glyph's lit pixels are merged into maximal rectangles so the native renderer draws\n\
         //! the fewest filled quads per glyph.\n\
         #![allow(clippy::all)]\n\
         use super::{{FaceData, Glyph, Rect}};\n"
    )
    .unwrap();

    emit_face(&mut src, "MENU", &menu);
    src.push('\n');
    emit_face(&mut src, "COMPACT", &compact);

    std::fs::write(&out, src).expect("write generated.rs");
    eprintln!(
        "wrote {} ({} menu glyphs, {} compact glyphs)",
        out.display(),
        menu.glyphs.len(),
        compact.glyphs.len()
    );
}

fn load(path: &Path) -> BdfFont {
    let text = std::fs::read_to_string(path).unwrap_or_else(|e| panic!("read {}: {e}", path.display()));
    bdf::parse(&text).unwrap_or_else(|e| panic!("parse {}: {e}", path.display()))
}

/// Emit one `pub static <NAME>: FaceData = …` block. Verifies the glyphs form the expected contiguous
/// printable-ASCII range with a constant advance (Proggy is monospaced); a gap or stray advance is a
/// generator-time panic rather than silently wrong tables.
fn emit_face(src: &mut String, name: &str, font: &BdfFont) {
    let first = 0x20u32;
    assert_eq!(font.glyphs[0].codepoint, first, "{name}: first glyph is not U+0020");
    let advance = font.glyphs[0].advance;
    for (i, g) in font.glyphs.iter().enumerate() {
        assert_eq!(g.codepoint, first + i as u32, "{name}: non-contiguous codepoints");
        assert_eq!(g.advance, advance, "{name}: non-monospaced glyph U+{:04X}", g.codepoint);
    }

    writeln!(src, "pub static {name}: FaceData = FaceData {{").unwrap();
    writeln!(src, "    cell_w: {},", font.cell_w).unwrap();
    writeln!(src, "    cell_h: {},", font.cell_h).unwrap();
    writeln!(src, "    ascent: {},", font.ascent).unwrap();
    writeln!(src, "    descent: {},", font.descent).unwrap();
    writeln!(src, "    advance: {advance},").unwrap();
    writeln!(src, "    first: 0x{first:02X},").unwrap();
    writeln!(src, "    glyphs: &[").unwrap();
    for g in &font.glyphs {
        let rects = merge::merge(&g.bitmap);
        let ch = char::from_u32(g.codepoint).unwrap();
        let label = if ch == ' ' { "space".to_string() } else { format!("'{ch}'") };
        write!(src, "        Glyph {{ rects: &[").unwrap();
        for (i, r) in rects.iter().enumerate() {
            if i > 0 {
                src.push_str(", ");
            }
            write!(src, "Rect {{ x: {}, y: {}, w: {}, h: {} }}", r.x, r.y, r.w, r.h).unwrap();
        }
        writeln!(src, "] }}, // U+{:04X} {label}", g.codepoint).unwrap();
    }
    writeln!(src, "    ],").unwrap();
    writeln!(src, "}};").unwrap();
}
